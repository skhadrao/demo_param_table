    myargs = [
        "Made in ",
        image('https://avatars3.githubusercontent.com/u/45109972?s=400&v=4',
              width=px(25), height=px(25)),
        " with ❤️ by ",
        link("https://confluence.ids.saint-gobain.com/display/DA/STREAMLIT", "@Saïd"),
        br(),
        link("https://confluence.ids.saint-gobain.com/display/DA/STREAMLIT", image(file_path)),
    ]
    layout(*myargs)
